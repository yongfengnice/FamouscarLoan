package com.famous.zhifu.loan.entity;

public class DetailPayTypeInfo {
	private String endtime;
	private int isrepayment;
	private String money;
	private String residuemoney;
	
	public String getEndtime() {
		return endtime;
	}
	public void setEndtime(String endtime) {
		this.endtime = endtime;
	}
	public int getIsrepayment() {
		return isrepayment;
	}
	public void setIsrepayment(int isrepayment) {
		this.isrepayment = isrepayment;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getResiduemoney() {
		return residuemoney;
	}
	public void setResiduemoney(String residuemoney) {
		this.residuemoney = residuemoney;
	}
	
	
}
