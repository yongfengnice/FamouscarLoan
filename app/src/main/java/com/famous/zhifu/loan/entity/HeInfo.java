package com.famous.zhifu.loan.entity;

public class HeInfo {
	private String id;
	private String money;
	private String tendermoney;
	private String ratemoney;
	private String status;
	private String lasttime;
	private String residuemoney;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getTendermoney() {
		return tendermoney;
	}
	public void setTendermoney(String tendermoney) {
		this.tendermoney = tendermoney;
	}
	public String getRatemoney() {
		return ratemoney;
	}
	public void setRatemoney(String ratemoney) {
		this.ratemoney = ratemoney;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getLasttime() {
		return lasttime;
	}
	public void setLasttime(String lasttime) {
		this.lasttime = lasttime;
	}
	public String getResiduemoney() {
		return residuemoney;
	}
	public void setResiduemoney(String residuemoney) {
		this.residuemoney = residuemoney;
	}
	
}
